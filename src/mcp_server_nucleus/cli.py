def _patch_mcp_config(config_path: Path, ide_name: str, nucleus_config: Dict[str, Any], force: bool = False):
    """Inject nucleus server into IDE-specific MCP configuration."""
    if not config_path.exists():
        return False
    try:
        print(f"\n🔍 Found {ide_name} config...")
        # Reversible: Create backup
        backup_path = config_path.with_suffix(".json.bak")
        if not backup_path.exists():
            shutil.copy2(config_path, backup_path)
            print(f"  📦 Created backup at {backup_path}")

        with open(config_path, 'r', encoding='utf-8') as f:
            config_data = json.load(f)

        # Normalize to standard 'mcpServers' block
        target_key = "mcpServers"
        if target_key not in config_data:
            config_data[target_key] = {}

        if "nucleus" not in config_data[target_key]:
            config_data[target_key]["nucleus"] = nucleus_config
            with open(config_path, 'w', encoding='utf-8') as f:
                json.dump(config_data, f, indent=2, ensure_ascii=False)
            print(f"  ✅ Auto-configured 'nucleus' in {ide_name} settings!")
            return True
        elif force:
            config_data[target_key]["nucleus"] = nucleus_config
            with open(config_path, 'w', encoding='utf-8') as f:
                json.dump(config_data, f, indent=2, ensure_ascii=False)
            print(f"  ✅ Overwrote 'nucleus' config in {ide_name}!")
            return True
        else:
            print(f"  ℹ️  'nucleus' already configured in {ide_name} (use --force to overwrite).")
            return True
    except Exception as e:
        print(f"  ⚠️  Could not auto-configure {ide_name}: {e}")
        return False

def init_brain(path: str = ".brain", template: str = "default") -> bool:
    """Initialize a new .brain directory structure."""
    brain_path = Path(path)
    
    if brain_path.exists():
        # Count files to detect active brain
        file_count = len(list(brain_path.rglob("*")))
        
        if file_count > 10:
            # This looks like an active brain - extra protection
            print(f"⚠️  {path}/ contains {file_count} files!")
            print("   This looks like an ACTIVE brain with real content.")
            print("   Overwriting is NOT recommended.")
            print()
            response = input("Type 'BACKUP-AND-OVERWRITE' to confirm (or anything else to abort): ")
            if response != 'BACKUP-AND-OVERWRITE':
                print("✅ Aborted. Your brain is safe.")
                return False
        else:
            print(f"⚠️  Directory {path} already exists.")
            response = input("Overwrite? (y/N): ")
            if response.lower() != 'y':
                print("Aborted.")
                return False
        
        # ALWAYS backup before overwrite
        timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
        backup_path = Path(f"{path}.backup.{timestamp}")
        print(f"📦 Creating backup at {backup_path}...")
        shutil.copytree(brain_path, backup_path)
        print(f"   ✅ Backup complete ({file_count} items saved)")
        
        # Now safe to remove old brain
        shutil.rmtree(brain_path)
    
    print(f"🧠 Initializing Nucleus Control Plane at {path}/ (template: {template})...")
    
    # Route to template-specific init
    if template == "solo":
        init_brain_solo(brain_path)
