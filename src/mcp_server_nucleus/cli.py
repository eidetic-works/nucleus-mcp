        # Federation status is derived from persistence and ephemeral metrics
        status = {
            "brain_id": brain_id,
            "region": region,
            "status": "ALIVE" if engine.running else "IDLE",
            "leader": engine.state.leader_id or "NONE",
            "term": engine.state.term,
            "partition": engine.state.partition_status.name,
            "peers_online": len([p for p in engine.state.peers.values() if p.is_online()]),
            "peers_total": len(engine.state.peers),
            "merkle_root": engine.sync.merkle_tree.get_root()
        }
